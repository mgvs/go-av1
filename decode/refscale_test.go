func TestReferenceScaling(t *testing.T) {
	frames := [][]byte{rszF0, rszF1, rszF2, rszF3, rszF4, rszF5}
	var seq *header.SequenceHeader
	var dec *Decoder
	for i, pl := range frames {
		obus, err := obu.Split(pl)
		if err != nil {
			t.Fatalf("f%d split: %v", i, err)
		}
		for _, o := range obus {
			switch o.Type {
			case obu.TypeSequenceHeader:
				seq, err = header.ParseSequenceHeader(o.Payload)
				if err != nil {
					t.Fatalf("seq: %v", err)
				}
				dec = NewDecoder(seq)
			case obu.TypeFrame:
				fh, off, err := header.ParseFrameHeader(seq, dec.State(), o.Payload, o.TemporalID, o.SpatialID)
				if err != nil {
					t.Fatalf("f%d header: %v", i, err)
				}
				tg, err := tile.SplitTileGroup(fh, o.Payload[off:])
				if err != nil {
					t.Fatalf("f%d tiles: %v", i, err)
				}
				fr, err := dec.DecodeFrame(fh, tg)
				if err != nil {
					t.Fatalf("f%d decode: %v", i, err)
				}
				var b []byte
				for _, pp := range fr.Planes {
					for _, v := range pp.Data {
						b = append(b, byte(v))
					}
				}
				h := sha256.Sum256(b)
				if got := hex.EncodeToString(h[:]); got != rszShas[i] {
					t.Errorf("f%d sha = %s, want %s", i, got, rszShas[i])
				}
			}
		}
	}
}
