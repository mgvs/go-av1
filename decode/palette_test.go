var palShas = []string{
	"3ee6d8667faa1134bf8a2eb224a1b7c8f306fe3b36a5c1699562cd88c3c09ba0",
}

func TestPalette(t *testing.T) {
	frames := [][]byte{palF0}
	var seq *header.SequenceHeader
	var dec *Decoder
	for i, pl := range frames {
		obus, _ := obu.Split(pl)
		for _, o := range obus {
			switch o.Type {
			case obu.TypeSequenceHeader:
				var err error
				seq, err = header.ParseSequenceHeader(o.Payload)
				if err != nil {
					t.Fatalf("seq: %v", err)
				}
				dec = NewDecoder(seq)
			case obu.TypeFrame:
				fh, off, err := header.ParseFrameHeader(seq, dec.State(), o.Payload, o.TemporalID, o.SpatialID)
				if err != nil {
					t.Fatalf("f%d header: %v", i, err)
				}
				tg, err := tile.SplitTileGroup(fh, o.Payload[off:])
				if err != nil {
					t.Fatalf("f%d tiles: %v", i, err)
				}
				fr, err := dec.DecodeFrame(fh, tg)
				if err != nil {
					t.Fatalf("f%d decode: %v", i, err)
				}
				var b []byte
				for _, pp := range fr.Planes {
					for _, v := range pp.Data {
						b = append(b, byte(v))
					}
				}
				h := sha256.Sum256(b)
				if got := hex.EncodeToString(h[:]); got != palShas[i] {
					t.Errorf("f%d sha = %s, want %s", i, got, palShas[i])
				}
			}
		}
	}
}
