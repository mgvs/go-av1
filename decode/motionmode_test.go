const mmF1SHA256 = "727bb3f207a65183fd5b382ad84783b799c48640055306142afea3f71ee466a1"

func TestDecodeInterMotionMode(t *testing.T) {
	o0, _ := obu.Split(mmF0)
	o1, _ := obu.Split(mmF1)
	var seq *header.SequenceHeader
	var dec *Decoder
	var frame *Frame
	for _, group := range [][]obu.OBU{o0, o1} {
		for _, o := range group {
			switch o.Type {
			case obu.TypeSequenceHeader:
				s, err := header.ParseSequenceHeader(o.Payload)
				if err != nil {
					t.Fatalf("seq: %v", err)
				}
				seq = s
				dec = NewDecoder(seq)
			case obu.TypeFrame:
				fh, off, err := header.ParseFrameHeader(seq, dec.State(), o.Payload, o.TemporalID, o.SpatialID)
				if err != nil {
					t.Fatalf("frame header: %v", err)
				}
				tiles, err := tile.SplitTileGroup(fh, o.Payload[off:])
				if err != nil {
					t.Fatalf("tile split: %v", err)
				}
				if frame, err = dec.DecodeFrame(fh, tiles); err != nil {
					t.Fatalf("decode: %v", err)
				}
			}
		}
	}
	hsh := sha256.New()
	for _, pl := range frame.Planes {
		buf := make([]byte, len(pl.Data))
		for i, v := range pl.Data {
			buf[i] = byte(v)
		}
		hsh.Write(buf)
	}
	if got := hex.EncodeToString(hsh.Sum(nil)); got != mmF1SHA256 {
		t.Fatalf("motion-mode inter reconstruction sha256 = %s want %s", got, mmF1SHA256)
	}
}
