func TestOBMCConformance66x66(t *testing.T) {
	frames := [][]byte{sz66F0, sz66F1}
	var seq *header.SequenceHeader
	var dec *Decoder
	si := 0
	for i, pl := range frames {
		obus, _ := obu.Split(pl)
		for _, o := range obus {
			switch o.Type {
			case obu.TypeSequenceHeader:
				var err error
				seq, err = header.ParseSequenceHeader(o.Payload)
				if err != nil {
					t.Fatalf("seq: %v", err)
				}
				dec = NewDecoder(seq)
			case obu.TypeFrame, obu.TypeFrameHeader:
				fh, off, err := header.ParseFrameHeader(seq, dec.State(), o.Payload, o.TemporalID, o.SpatialID)
				if err != nil {
					t.Fatalf("f%d header: %v", i, err)
				}
				var tg []tile.Tile
				if o.Type == obu.TypeFrame {
					tg, err = tile.SplitTileGroup(fh, o.Payload[off:])
					if err != nil {
						t.Fatalf("f%d tiles: %v", i, err)
					}
				}
				fr, err := dec.DecodeFrame(fh, tg)
				if err != nil {
					t.Fatalf("f%d decode: %v", i, err)
				}
				if fr == nil || !(fh.ShowFrame || fh.ShowExistingFrame) {
					continue
				}
				var b []byte
				for _, pp := range fr.Planes {
					for _, v := range pp.Data {
						b = append(b, byte(v))
					}
				}
				h := sha256.Sum256(b)
				if got := hex.EncodeToString(h[:]); got != sz66Shas[si] {
					t.Errorf("shown-frame %d sha = %s, want %s", si, got, sz66Shas[si])
				}
				si++
			}
		}
	}
}
