const varTxF1SHA256 = "9491aa719e5be1648b8adc741fbc11d2d7e7b691f378d1d79d19a0e4bdc42d36"

func TestDecodeInterVarTx(t *testing.T) {
	o0, _ := obu.Split(varTxF0)
	o1, _ := obu.Split(varTxF1)
	var seq *header.SequenceHeader
	var dec *Decoder
	var frame *Frame
	for _, group := range [][]obu.OBU{o0, o1} {
		for _, o := range group {
			switch o.Type {
			case obu.TypeSequenceHeader:
				s, err := header.ParseSequenceHeader(o.Payload)
				if err != nil {
					t.Fatalf("seq: %v", err)
				}
				seq = s
				dec = NewDecoder(seq)
			case obu.TypeFrame:
				fh, off, err := header.ParseFrameHeader(seq, dec.State(), o.Payload, o.TemporalID, o.SpatialID)
				if err != nil {
					t.Fatalf("frame header: %v", err)
				}
				tiles, err := tile.SplitTileGroup(fh, o.Payload[off:])
				if err != nil {
					t.Fatalf("tile split: %v", err)
				}
				if frame, err = dec.DecodeFrame(fh, tiles); err != nil {
					t.Fatalf("decode: %v", err)
				}
			}
		}
	}
	hsh := sha256.New()
	for _, pl := range frame.Planes {
		buf := make([]byte, len(pl.Data))
		for i, v := range pl.Data {
			buf[i] = byte(v)
		}
		hsh.Write(buf)
	}
	if got := hex.EncodeToString(hsh.Sum(nil)); got != varTxF1SHA256 {
		t.Fatalf("var-tx inter reconstruction sha256 = %s want %s", got, varTxF1SHA256)
	}
}
