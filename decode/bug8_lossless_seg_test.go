// TestBug8SegmentQindexLossless decodes an ROI-mapped stream where base_q_idx=0
// and segmentation (SEG_LVL_ALT_Q) makes some segments lossless and others not.
// This exercises two frame-vs-segment fixes that aomenc/SVT never trigger:
//   - intra_tx_type is gated on the PER-SEGMENT qindex (get_qindex(1,segment_id)),
//     not the frame base_q_idx which is 0 here (AV1 spec §5.11.47); and
//   - per-block transform size / type / inverse transform use the PER-SEGMENT
//     Lossless (LosslessArray[segment_id]), not the frame-level CodedLossless.
//
// Before these fixes the stream desynced and panicked; the vector is byte-exact
// with dav1d.
func TestBug8SegmentQindexLossless(t *testing.T) {
	var tus [][]byte
	for p := 32; p+12 <= len(bug8IVF); {
		sz := int(binary.LittleEndian.Uint32(bug8IVF[p : p+4]))
		p += 12
		if p+sz > len(bug8IVF) {
			break
		}
		tus = append(tus, bug8IVF[p:p+sz])
		p += sz
	}
	var seq *header.SequenceHeader
	var dec *Decoder
	var curFh *header.FrameHeader
	var curTiles []tile.Tile
	si := 0
	fin := func(fh *header.FrameHeader, tg []tile.Tile) {
		fr, err := dec.DecodeFrame(fh, tg)
		if err != nil {
			t.Fatalf("shown frame %d decode: %v", si, err)
		}
		if fr == nil || !(fh.ShowFrame || fh.ShowExistingFrame) {
			return
		}
		var b []byte
		for _, pp := range fr.Planes {
			for _, v := range pp.Data {
				b = append(b, byte(v))
			}
		}
		h := sha256.Sum256(b)
		if got := hex.EncodeToString(h[:]); got != bug8Shas[si] {
			t.Errorf("shown frame %d sha = %s, want %s", si, got, bug8Shas[si])
		}
		si++
	}
	for _, tu := range tus {
		obus, _ := obu.Split(tu)
		for _, o := range obus {
			switch o.Type {
			case obu.TypeSequenceHeader:
				var err error
				if seq, err = header.ParseSequenceHeader(o.Payload); err != nil {
					t.Fatalf("seq: %v", err)
				}
				dec = NewDecoder(seq)
			case obu.TypeFrame:
				fh, off, err := header.ParseFrameHeader(seq, dec.State(), o.Payload, o.TemporalID, o.SpatialID)
				if err != nil {
					t.Fatalf("frame header: %v", err)
				}
				tg, err := tile.SplitTileGroup(fh, o.Payload[off:])
				if err != nil {
					t.Fatalf("tiles: %v", err)
				}
				fin(fh, tg)
			case obu.TypeFrameHeader:
				fh, _, err := header.ParseFrameHeader(seq, dec.State(), o.Payload, o.TemporalID, o.SpatialID)
				if err != nil {
					t.Fatalf("frame header: %v", err)
				}
				curFh, curTiles = fh, nil
				if fh.ShowExistingFrame {
					fin(fh, nil)
				}
			case obu.TypeTileGroup:
				if curFh == nil {
					continue
				}
				tg, err := tile.SplitTileGroup(curFh, o.Payload)
				if err != nil {
					t.Fatalf("tiles: %v", err)
				}
				curTiles = append(curTiles, tg...)
				if len(curTiles) >= curFh.TileCols*curFh.TileRows {
					fin(curFh, curTiles)
					curFh = nil
				}
			}
		}
	}
	if si != len(bug8Shas) {
		t.Fatalf("decoded %d shown frames, want %d", si, len(bug8Shas))
	}
}

