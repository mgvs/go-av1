func TestCmp4WedgeCompound(t *testing.T) {
	d := cmp4Ivf
	var seq *header.SequenceHeader
	var dec *Decoder
	p := 32
	fi := 0
	for p+12 <= len(d) {
		sz := int(binary.LittleEndian.Uint32(d[p : p+4]))
		p += 12
		if p+sz > len(d) {
			break
		}
		obus, err := obu.Split(d[p : p+sz])
		if err != nil {
			t.Fatalf("split: %v", err)
		}
		p += sz
		for _, o := range obus {
			switch o.Type {
			case obu.TypeSequenceHeader:
				seq, err = header.ParseSequenceHeader(o.Payload)
				if err != nil {
					t.Fatalf("seq: %v", err)
				}
				dec = NewDecoder(seq)
			case obu.TypeFrame:
				fh, off, err := header.ParseFrameHeader(seq, dec.State(), o.Payload, o.TemporalID, o.SpatialID)
				if err != nil {
					t.Fatalf("f%d header: %v", fi, err)
				}
				tg, err := tile.SplitTileGroup(fh, o.Payload[off:])
				if err != nil {
					t.Fatalf("f%d tiles: %v", fi, err)
				}
				fr, err := dec.DecodeFrame(fh, tg)
				if err != nil {
					t.Fatalf("f%d decode: %v", fi, err)
				}
				var b []byte
				for _, pl := range fr.Planes {
					for _, v := range pl.Data {
						b = append(b, byte(v))
					}
				}
				h := sha256.Sum256(b)
				if got := hex.EncodeToString(h[:]); got != cmp4Shas[fi] {
					t.Errorf("f%d sha = %s, want %s", fi, got, cmp4Shas[fi])
				}
				fi++
			}
		}
	}
	if fi != 12 {
		t.Fatalf("decoded %d frames, want 12", fi)
	}
}
