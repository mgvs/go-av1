// TestSegLvlSkipRefFrame decodes a 14-frame AV1 stream that forces SEG_LVL_SKIP
// (a forced-skip segment) and SEG_LVL_REF_FRAME (a forced-reference segment) via
// an ROI map — segment features that aomenc/SVT-AV1 never emit in normal use.
// The vector was produced by a libaom realtime ROI encoder and is byte-exact with
// dav1d. It exercises the segment-feature gates in read_skip / read_skip_mode /
// read_is_inter / read_ref_frames / read_inter_mode (AV1 spec §5.11.10/20/23/25).
func TestSegLvlSkipRefFrame(t *testing.T) {
	var tus [][]byte
	for p := 32; p+12 <= len(segRoiIVF); {
		sz := int(binary.LittleEndian.Uint32(segRoiIVF[p : p+4]))
		p += 12
		if p+sz > len(segRoiIVF) {
			break
		}
		tus = append(tus, segRoiIVF[p:p+sz])
		p += sz
	}

	var seq *header.SequenceHeader
	var dec *Decoder
	var curFh *header.FrameHeader
	var curTiles []tile.Tile
	si := 0
	fin := func(fh *header.FrameHeader, tg []tile.Tile) {
		fr, err := dec.DecodeFrame(fh, tg)
		if err != nil {
			t.Fatalf("shown frame %d decode: %v", si, err)
		}
		if fr == nil || !(fh.ShowFrame || fh.ShowExistingFrame) {
			return
		}
		var b []byte
		for _, pp := range fr.Planes {
			for _, v := range pp.Data {
				b = append(b, byte(v))
			}
		}
		h := sha256.Sum256(b)
		if got := hex.EncodeToString(h[:]); got != segRoiShas[si] {
			t.Errorf("shown frame %d sha = %s, want %s", si, got, segRoiShas[si])
		}
		si++
	}
	for _, tu := range tus {
		obus, _ := obu.Split(tu)
		for _, o := range obus {
			switch o.Type {
			case obu.TypeSequenceHeader:
				var err error
				if seq, err = header.ParseSequenceHeader(o.Payload); err != nil {
					t.Fatalf("seq: %v", err)
				}
				dec = NewDecoder(seq)
			case obu.TypeFrame:
				fh, off, err := header.ParseFrameHeader(seq, dec.State(), o.Payload, o.TemporalID, o.SpatialID)
				if err != nil {
					t.Fatalf("frame header: %v", err)
				}
				tg, err := tile.SplitTileGroup(fh, o.Payload[off:])
				if err != nil {
					t.Fatalf("tiles: %v", err)
				}
				fin(fh, tg)
			case obu.TypeFrameHeader:
				fh, _, err := header.ParseFrameHeader(seq, dec.State(), o.Payload, o.TemporalID, o.SpatialID)
				if err != nil {
					t.Fatalf("frame header: %v", err)
				}
				curFh, curTiles = fh, nil
				if fh.ShowExistingFrame {
					fin(fh, nil)
				}
			case obu.TypeTileGroup:
				if curFh == nil {
					continue
				}
				tg, err := tile.SplitTileGroup(curFh, o.Payload)
				if err != nil {
					t.Fatalf("tiles: %v", err)
				}
				curTiles = append(curTiles, tg...)
				if len(curTiles) >= curFh.TileCols*curFh.TileRows {
					fin(curFh, curTiles)
					curFh = nil
				}
			}
		}
	}
	if si != len(segRoiShas) {
		t.Fatalf("decoded %d shown frames, want %d", si, len(segRoiShas))
	}
}

