const interF1SHA256 = "2e924bcffb65d17826e326f861dfc6299ec2aaf1bb60c29ab9eed15047dd0e25"

func TestDecodeInterFrame(t *testing.T) {
	obus0, _ := obu.Split(interF0)
	obus1, _ := obu.Split(interF1)
	var seq *header.SequenceHeader
	var dec *Decoder
	var frame *Frame
	for _, group := range [][]obu.OBU{obus0, obus1} {
		for _, o := range group {
			switch o.Type {
			case obu.TypeSequenceHeader:
				s, err := header.ParseSequenceHeader(o.Payload)
				if err != nil {
					t.Fatalf("seq: %v", err)
				}
				seq = s
				dec = NewDecoder(seq)
			case obu.TypeFrame:
				fh, off, err := header.ParseFrameHeader(seq, dec.State(), o.Payload, o.TemporalID, o.SpatialID)
				if err != nil {
					t.Fatalf("frame header: %v", err)
				}
				tiles, err := tile.SplitTileGroup(fh, o.Payload[off:])
				if err != nil {
					t.Fatalf("tile split: %v", err)
				}
				if frame, err = dec.DecodeFrame(fh, tiles); err != nil {
					t.Fatalf("decode: %v", err)
				}
			}
		}
	}
	hsh := sha256.New()
	for _, pl := range frame.Planes {
		buf := make([]byte, len(pl.Data))
		for i, v := range pl.Data {
			buf[i] = byte(v)
		}
		hsh.Write(buf)
	}
	if got := hex.EncodeToString(hsh.Sum(nil)); got != interF1SHA256 {
		t.Fatalf("inter frame reconstruction sha256 = %s want %s", got, interF1SHA256)
	}
}
